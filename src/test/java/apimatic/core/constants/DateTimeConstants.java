package apimatic.core.constants;

/**
 * A class to store constant values for date and time used in tests.
 */
public final class DateTimeConstants {

    /**
     * Private constructor to prevent instantiation of this utility class.
     */
    private DateTimeConstants() {
        // Prevent instantiation
    }

    /** UNIX timestamp representing 1997-06-16 00:00:00 UTC. */
    public static final long UNIXTIMESTAMP3 = 868756200L;

    /** UNIX timestamp representing 2020-07-25 06:10:00 UTC. */
    public static final long UNIXTIMESTAMP2 = 1595639400L;

    /** UNIX timestamp representing 2000-07-13 06:10:00 UTC. */
    public static final long UNIXTIMESTAMP1 = 963450600L;

    /** Day value for the 25th. */
    public static final int DAY25 = 25;

    /** Year value for 2020. */
    public static final int YEAR2020 = 2020;

    /** Year value for 2000. */
    public static final int YEAR2000 = 2000;

    /** Hour value for 6 AM. */
    public static final int HOUR6 = 6;

    /** Day value for the 13th. */
    public static final int DAY13 = 13;

    /** Month value for July. */
    public static final int JULY = 7;

    /** Minute value for 10 minutes. */
    public static final int MINUTES10 = 10;

    /** Year value for 1997. */
    public static final int YEAR1997 = 1997;
}
